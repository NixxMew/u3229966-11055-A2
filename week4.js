// Canvas setup
function setup() {
  createCanvas(500, 500);
}

// Drawing the logo
function draw() {
  background(50);
  strokeWeight(0)
  square( 100, 100, 300 );
  fill('black')
  square(150, 150, 75)
  square(275, 150, 75)
  rect(225, 225, 50, 75)
  rect(175, 260, 50, 90)
  rect(275, 260, 50, 90)
  fill('green')
}
